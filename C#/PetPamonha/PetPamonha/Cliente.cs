using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace PetPamonha
{
    class Cliente
    {


        private String nome;
        private String cpf;
        private String email;
        private String telefone;

        



        public String Nome
        {


            get
            {
                return this.nome;
            }



            set
            {
                this.nome = value;
            }


        }

        public String CPF
        {


            get
            {
                return this.cpf;
            }



            set
            {
                this.cpf = value;
            }


        }

        public String Email
        {


            get
            {
                return this.email;
            }



            set
            {
                this.email = value;
            }


        }

        public String Telefone
        {


            get
            {
                return this.telefone;
            }



            set
            {
                this.telefone = value;
            }


        }











    }
}
